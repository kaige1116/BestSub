package country

type Country struct {
	NameEn string
	NameZh string
	Emoji  string
}

func GetCountry(code string) Country {
	if code == "" {
		return Country{}
	}
	if names, ok := namesByNumeric[code]; ok {
		return Country{NameEn: code, NameZh: names, Emoji: emoji(code)}
	}
	return Country{}
}

func emoji(alpha2 string) string {
	if len(alpha2) != 2 {
		return ""
	}
	b0 := rune(alpha2[0])
	b1 := rune(alpha2[1])
	if b0 >= 'a' && b0 <= 'z' {
		b0 -= 32
	}
	if b1 >= 'a' && b1 <= 'z' {
		b1 -= 32
	}
	if b0 < 'A' || b0 > 'Z' || b1 < 'A' || b1 > 'Z' {
		return ""
	}
	r0 := 0x1F1E6 + (b0 - 'A')
	r1 := 0x1F1E6 + (b1 - 'A')
	return string([]rune{r0, r1})
}

